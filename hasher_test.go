package main

import (
	"crypto/sha512"
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestDefaultSha512Hash(t *testing.T) {
	hasher := NewSha512Hasher()
	assert.Equal(t, sha512.Size, hasher.Size())
	hash, err := hasher.Hash("Hello World")
	assert.NoError(t, err)

	expected := []byte{0x2c, 0x74, 0xfd, 0x17, 0xed, 0xaf, 0xd8, 0xe, 0x84, 0x47, 0xb0, 0xd4, 0x67, 0x41, 0xee, 0x24, 0x3b, 0x7e, 0xb7, 0x4d, 0xd2, 0x14, 0x9a, 0xa, 0xb1, 0xb9, 0x24, 0x6f, 0xb3, 0x3, 0x82, 0xf2, 0x7e, 0x85, 0x3d, 0x85, 0x85, 0x71, 0x9e, 0xe, 0x67, 0xcb, 0xda, 0xd, 0xaa, 0x8f, 0x51, 0x67, 0x10, 0x64, 0x61, 0x5d, 0x64, 0x5a, 0xe2, 0x7a, 0xcb, 0x15, 0xbf, 0xb1, 0x44, 0x7f, 0x45, 0x9b}
	assert.Equal(t, hasher.Size(), len(hash))
	assert.Equal(t, hasher.Size(), len(expected))
	assert.Equal(t, expected, hash)
}
